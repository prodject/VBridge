// generateName generates a random Russian name for VK call identity.
// 30% chance first name only, 70% first + last name.
// Female names get feminized last names (appends "а").
func generateName() string {
	fn := firstNames[mathrand.Intn(len(firstNames))]

	if mathrand.Float32() < 0.3 {
		return fn
	}

	ln := lastNames[mathrand.Intn(len(lastNames))]

	// Feminize surname for female first names (ending in 'а' or 'я')
	fnBytes := []byte(fn)
	if len(fnBytes) >= 2 {
		lastChar := string(fnBytes[len(fnBytes)-2:]) // 2 bytes for Cyrillic
		if lastChar == "\u0430" || lastChar == "\u044f" {
			return fmt.Sprintf("%s %s\u0430", fn, ln)
		}
	}
	return fmt.Sprintf("%s %s", fn, ln)
}
